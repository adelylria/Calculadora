package app

// Sumar toma dos números y devuelve su suma.
func Sumar(a float64, b float64) float64 {
	return a + b
}

// Restar toma dos números y devuelve su resta.
func Restar(a float64, b float64) float64 {
	return a - b
}

// Division toma dos números y devuelve el resultado de la división entre ellos.
func Division(a float64, b float64) float64 {
	return a / b
}

// Multiplicar toma dos números y devuelve su producto.
func Multiplicar(a float64, b float64) float64 {
	return a * b
}
